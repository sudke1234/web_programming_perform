function showContent() {
    const p1 = document.getElementById('problem1');
    const content = "소각<br>심각한 대기오염의 원인"; //가장 많이 쓰이는 쓰레기 처리 방법이다.
    p1.style.color = "black";
    p1.style.fontSize = "25px"; 
    p1.innerHTML = content;
  }
  
  function hideContent() {
    const p1 = document.getElementById('problem1');
    p1.style.backgroundColor = ''; // 기존 배경색으로 돌아가게 설정
    p1.innerHTML = ''; //업슴
  }

